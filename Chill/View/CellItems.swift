import Foundation
import UIKit

func setUpLabel(label: UILabel) -> UILabel {
    label.textColor = .white
    label.backgroundColor = UIColor(red: 0, green: 0, blue: 0, alpha: 0.6)
    label.translatesAutoresizingMaskIntoConstraints = false
    return label
}

